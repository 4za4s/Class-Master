function getTime() {
    var time = new Date() 
    Hour = time.getHours()
    if (Hour < 10){
        Hour = "0" + Hour    
    }
    Minute = time.getMinutes()
    if (Minute < 10){
        Minute = "0" + Minute
    }
    time =  Hour + ":" + Minute
    document.getElementById("Time").innerHTML = time;

    
}

